    private void btBorrarSegmentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarSegmentoActionPerformed
        int fila = jtSegmentos.getSelectedRow();
        if(fila != -1){
            if(jtSegmentos.getRowCount() > 1){
                modeloTablaSegmentos.removeRow(fila);
                int cantidadFilas = jtSegmentos.getRowCount();
                for(int i = 0; i < cantidadFilas; i++){
                    modeloTablaSegmentos.setValueAt("Segmento "+i, i, 0);
                }
            }
        }
    }//GEN-LAST:event_btBorrarSegmentoActionPerformed


