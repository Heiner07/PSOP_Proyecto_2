    private void agregarSegmento(int tamanio){
        this.modeloTablaSegmentos.addRow(new Object[]{ "Segmento "+modeloTablaSegmentos.getRowCount(), tamanio });
    }
    
    public int[] obtenerSegmentos(){
        int cantidadFilas = modeloTablaSegmentos.getRowCount();
        int[] tamanioSegmentos = new int[cantidadFilas];
        for(int i = 0; i < cantidadFilas; i++){
            tamanioSegmentos[i] = (int)modeloTablaSegmentos.getValueAt(i, 1);
        }
        return tamanioSegmentos;
    }

